<template>
  <div id="app">
    <router-view v-if="isRouterAlive" />
  </div>
</template>
<script>
export default {
  provide() {
    return {
      reload: this.reloadFn,
    };
  },
  data() {
    return {
      isRouterAlive: true,
      testDate: 'zhelishi git ceshi',
    };
  },
  methods: {
    reloadFn() {
      this.isRouterAlive = false;
      this.$nextTick(() => {
        this.isRouterAlive = true;
      });
    },
  },
};
</script>
